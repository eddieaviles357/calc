"""Basic math operations."""

def add(a, b):
    """Add a and b.
    >>> add(3, 5)
    8
    """
    return a + b

def sub(a, b):
    """Substract b from a.
    >>> sub(5, 3)
    2
    """

    return a - b

def mult(a, b):
    """Multiply a and b.
    >>> mult(3, 5)
    15
    """

    return a * b

def div(a, b):
    """Divide a by b.
    >>> div(6, 3)
    2
    """

    return a / b
